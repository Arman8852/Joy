                            <h3 style="color:black;"><b>Summary:</b></h3>
                        </p>
                        
                        <p style='text-align: justify;'>We have implemented an Ensembling Technique which combines a Convolutional Neural Network with Support Vector Regression. Convolutional Neural Network helps us to extract essential features, and Support Vector regression benefits us in making a robust model. Si-based materials data has been collected from a database we use to train and test our samples. We use Grid Search CV and more advanced Bayesian Optimization Techniques to find the best hyperparameters. By building this model, we can predict formation energy and packing fraction from any real-world Si-based structure. We selected three types of feature representations named XRD diffraction Pattern, Sine Coulomb Matrix, and Orbital Field Matrix to build our model. Our work highlights the opportunities of Ensembling Neural networks with a conventional Machine learning model to predict the stability of new Si-based anode materials beyond traditional graphite anode batteries. It also paves the way by helping the experimentalist select proper battery materials before experimenting.</p>
